#include <stdio.h>


int main(void)
{
	int A[3];
	for (int i = 0; i < 3; i++) {
		scanf("%d", &A[i]);
	}
	int multi = A[0] * A[1] * A[2];
	
	int s[10];
	s[0] = multi / 1000000000;
	if (multi % 1000000000 == multi) s[0] = 10;
	s[1] = multi % 1000000000 / 100000000;
	if (multi % 1000000000 % 100000000 == multi) s[1] = 10;
	s[2] = multi % 1000000000 % 100000000 / 10000000;
	if (multi % 1000000000 % 100000000 % 10000000 == multi) s[2] = 10;
	s[3] = multi % 1000000000 % 100000000 % 10000000 / 1000000;
	if (multi % 1000000000 % 100000000 % 10000000 % 1000000 == multi) s[3] = 10;
	s[4] = multi % 1000000000 % 100000000 % 10000000 % 1000000 / 100000;
	s[5] = multi % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 / 10000;
	s[6] = multi % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 % 10000 / 1000;
	s[7] = multi % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 % 10000 % 1000 / 100;
	s[8] = multi % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 % 10000 % 1000 % 100 / 10;
	s[9] = multi % 1000000000 % 100000000 % 10000000 % 1000000 % 100000 % 10000 % 1000 % 100 % 10;
	
	int num[10] = { 0 };
	for (int i = 0; i < 10; i++) {
		if (s[i] < 10) {
			num[s[i]] += 1;
		}
	}

	for (int i = 0; i < 10; i++) {
		printf("%d\n", num[i]);
	}

	return 0;
}